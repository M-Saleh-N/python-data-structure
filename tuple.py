# TUPLE
"""
= Tuples are similar to list but immutable (cannot change) once created 
Properties:-
    * Ordered
    * Immutable
    * Faster than list
    * Good for fixed data

"""
names = ("Saleh", "Vansh", "Tilak", "Ansh", "Esha")
# INDEXING
print(names[0])  # Saleh
# Tuple unpacking
a,b,c,d,e = names
print(a)  # Saleh 
